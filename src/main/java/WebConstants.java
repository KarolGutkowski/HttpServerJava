// I define here constants useful to implementation of HTTP 1.1 standard
// SP - space
// CR - carriage return
// LF - line feed (new line)
// CRLF - combination of the above two
public class WebConstants {
    public final static Character SP = ' ';
    public final static Character CR = '\r';
    public final static Character LF = '\n';
    public final static String CRLF = "" + CR + LF;
}
